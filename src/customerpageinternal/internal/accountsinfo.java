    private void confirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmMouseClicked
        if(action.equals("Add")){
            dbconnector dbc = new dbconnector();
            int result = dbc.insertData("INSERT INTO tbl_account (acc_type, acc_username , acc_email, acc_password) " + "VALUES ('"+accounttype.getText()+"', '"+accountusername.getText()+"','"+accountemail.getText()+"',"
                + "'"+accountpassword.getText()+'"');
            if(result == 1){
                JOptionPane.showMessageDialog(null, "Success! Very Nice.");
                close();
            }else{
                JOptionPane.showMessageDialog(null, "Adding Data Failed");
            }
        }else if(action.equals("Edit")){
            dbconnector dbc = new dbconnector();
            dbc.updateData("UPDATE tbl_account SET acc_type = '"+accounttype.getText()+"', "
                + "acc_username = '"+accountusername.getText()+"', acc_email = '"+accountemail.getText()+"', acc_password = '"+accountpassword.getText()+"', '"
                + "WHERE   acc_id = '"+accountid.getText()+"'");
                close();
            }else{
                JOptionPane.showMessageDialog(null, "No Actions Performed!");
                close();
            }
    }//GEN-LAST:event_confirmMouseClicked

